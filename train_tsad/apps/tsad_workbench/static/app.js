function pollJob(kind, jobId) {
  if (workbenchState.polling[kind]) {
    window.clearInterval(workbenchState.polling[kind]);
  }
  const tick = async () => {
    try {
      const payload = await fetchJson(`/api/job?job_id=${encodeURIComponent(jobId)}`);
      if (kind === "generate") {
        workbenchDom.runSummary.textContent = safeJson(payload.result ?? { status: payload.status, logs: payload.logs?.slice(-30) ?? [] });
      } else if (kind === "train") {
        renderTrainingJob(payload);
      }
      if (payload.status === "completed") {
        window.clearInterval(workbenchState.polling[kind]);
        workbenchState.polling[kind] = null;
        if (kind === "generate" && payload.result) {
          applyRunInfo(payload.result);
        }
        if (kind === "train" && payload.result) {
          workbenchDom.trainOutputDir.value = payload.result.output_dir ?? workbenchDom.trainOutputDir.value;
          refreshTrainingMetrics();
        }
      }
      if (payload.status === "failed") {
        window.clearInterval(workbenchState.polling[kind]);
        workbenchState.polling[kind] = null;
      }
    } catch (error) {
      if (kind === "train") {
        workbenchDom.trainingLog.textContent = String(error.message ?? error);
      }
    }
  };
  tick();
  workbenchState.polling[kind] = window.setInterval(tick, 2000);
}

function renderTrainingJob(job) {
  const chips = [
    `job ${job.job_id}`,
    `status ${job.status}`,
    job.started_at ? `started ${new Date(job.started_at * 1000).toLocaleTimeString()}` : null,
    job.finished_at ? `finished ${new Date(job.finished_at * 1000).toLocaleTimeString()}` : null,
  ].filter(Boolean);
  workbenchDom.trainingStatus.innerHTML = chips
    .map((value) => `<span class="stat-pill">${escapeHtml(String(value))}</span>`)
    .join("");
  workbenchDom.trainingLog.textContent = (job.logs ?? []).slice(-120).join("\n");
  if (job.error) {
    workbenchDom.trainingSummary.textContent = safeJson({ error: job.error, result: job.result });
  }
}

async function refreshTrainingMetrics() {
  const outputDir = workbenchDom.trainOutputDir.value.trim();
  const runRoot = workbenchDom.runPath.value.trim();
  if (!outputDir && !runRoot) {
    return;
  }
  try {
    const payload = await fetchJson(buildQuery("/api/train-metrics", {
      output_dir: outputDir,
      run_root: runRoot,
    }));
    workbenchDom.trainingSummary.textContent = safeJson({
      summary: payload.summary,
      data_quality_report: payload.data_quality_report?.summary ?? payload.data_quality_report,
    });
    const historyView = payload.history_view ?? {};
    if (historyView.preferred_loss) {
      drawMetricChart(workbenchDom.lossCanvas, historyView.epochs ?? [], historyView.series ?? {}, [historyView.preferred_loss]);
    }
    if (historyView.preferred_quality) {
      drawMetricChart(workbenchDom.metricCanvas, historyView.epochs ?? [], historyView.series ?? {}, [historyView.preferred_quality]);
    }
  } catch (error) {
    workbenchDom.trainingSummary.textContent = safeJson({ error: error.message ?? String(error) });
  }
}

async function postJson(url, body) {
  const response = await fetch(url, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(body),
  });
  const payload = await response.json();
  if (!response.ok) {
    throw new Error(payload.error ?? `Request failed: ${response.status}`);
  }
  return payload;
}

async function fetchJson(url) {
  const response = await fetch(url);
  const payload = await response.json();
  if (!response.ok) {
    throw new Error(payload.error ?? `Request failed: ${response.status}`);
  }
  return payload;
}

function buildQuery(path, params) {
  const query = new URLSearchParams();
  Object.entries(params).forEach(([key, value]) => {
    if (value !== undefined && value !== null && value !== "") {
      query.set(key, String(value));
    }
  });
  return `${path}?${query.toString()}`;
}

function safeJson(value) {
  return JSON.stringify(value, null, 2);
}

function drawSeriesPairChart(canvas, primary, reference, mask, paddingMask) {
  const context = canvas.getContext("2d");
  const width = canvas.width;
  const height = canvas.height;
  context.clearRect(0, 0, width, height);
  context.fillStyle = "#ffffff";
  context.fillRect(0, 0, width, height);
  if (!primary?.length) {
    return;
  }
  const padding = { left: 56, right: 20, top: 18, bottom: 34 };
  const plotWidth = width - padding.left - padding.right;
  const plotHeight = height - padding.top - padding.bottom;
  const values = [...primary, ...reference].filter((value) => Number.isFinite(value));
  let minValue = Math.min(...values);
  let maxValue = Math.max(...values);
  if (!Number.isFinite(minValue) || !Number.isFinite(maxValue) || Math.abs(maxValue - minValue) < 1e-8) {
    minValue = -1;
    maxValue = 1;
  }
  if (mask?.length) {
    context.fillStyle = "rgba(181, 82, 50, 0.08)";
    mask.forEach((value, index) => {
      if (!value) return;
      const x = padding.left + (index / Math.max(1, primary.length - 1)) * plotWidth;
      context.fillRect(x, padding.top, Math.max(2, plotWidth / Math.max(primary.length, 1)), plotHeight);
    });
  }
  if (paddingMask?.length) {
    context.fillStyle = "rgba(40, 83, 107, 0.08)";
    paddingMask.forEach((value, index) => {
      if (!value) return;
      const x = padding.left + (index / Math.max(1, primary.length - 1)) * plotWidth;
      context.fillRect(x, padding.top, Math.max(2, plotWidth / Math.max(primary.length, 1)), plotHeight);
    });
  }
  drawAxes(context, padding, width, height, minValue, maxValue, primary.length);
  drawSimpleLine(context, primary, padding, plotWidth, plotHeight, minValue, maxValue, "#b55232", 2.2);
  if (reference?.length) {
    drawSimpleLine(context, reference, padding, plotWidth, plotHeight, minValue, maxValue, "#28536b", 1.8);
  }
}

function drawSimpleLine(context, values, padding, plotWidth, plotHeight, minValue, maxValue, color, width) {
  context.strokeStyle = color;
  context.lineWidth = width;
  context.beginPath();
  values.forEach((value, index) => {
    const x = padding.left + (index / Math.max(1, values.length - 1)) * plotWidth;
    const ratio = (value - minValue) / Math.max(1e-8, maxValue - minValue);
    const y = padding.top + plotHeight - ratio * plotHeight;
    if (index === 0) {
      context.moveTo(x, y);
    } else {
      context.lineTo(x, y);
    }
  });
  context.stroke();
}

function drawPatchBarChart(canvas, patchLabels) {
  const context = canvas.getContext("2d");
  const width = canvas.width;
  const height = canvas.height;
  context.clearRect(0, 0, width, height);
  context.fillStyle = "#ffffff";
  context.fillRect(0, 0, width, height);
  if (!patchLabels?.length) {
    return;
  }
  const padding = { left: 20, right: 20, top: 30, bottom: 24 };
  const barWidth = (width - padding.left - padding.right) / patchLabels.length;
  patchLabels.forEach((value, index) => {
    context.fillStyle = value ? "#b55232" : "#d8cdbd";
    context.fillRect(padding.left + index * barWidth, padding.top, Math.max(2, barWidth - 1), height - padding.top - padding.bottom);
  });
}

function drawMetricChart(canvas, epochs, seriesMap, metricNames) {
  const context = canvas.getContext("2d");
  const width = canvas.width;
  const height = canvas.height;
  context.clearRect(0, 0, width, height);
  context.fillStyle = "#ffffff";
  context.fillRect(0, 0, width, height);
  const activeNames = (metricNames ?? []).filter((name) => Array.isArray(seriesMap?.[name]));
  if (!activeNames.length) {
    return;
  }
  const values = activeNames.flatMap((name) => seriesMap[name].filter((value) => Number.isFinite(value)));
  let minValue = Math.min(...values);
  let maxValue = Math.max(...values);
  if (!Number.isFinite(minValue) || !Number.isFinite(maxValue) || Math.abs(maxValue - minValue) < 1e-8) {
    minValue -= 1;
    maxValue += 1;
  }
  const padding = { left: 56, right: 20, top: 18, bottom: 34 };
  const plotWidth = width - padding.left - padding.right;
  const plotHeight = height - padding.top - padding.bottom;
  drawAxes(context, padding, width, height, minValue, maxValue, Math.max(epochs.length, 1));
  activeNames.forEach((name, index) => {
    const valuesForLine = seriesMap[name].map((value) => (Number.isFinite(value) ? value : null));
    context.strokeStyle = palette[index % palette.length];
    context.lineWidth = 2;
    context.beginPath();
    valuesForLine.forEach((value, pointIndex) => {
      if (value == null) {
        return;
      }
      const x = padding.left + (pointIndex / Math.max(1, valuesForLine.length - 1)) * plotWidth;
      const ratio = (value - minValue) / Math.max(1e-8, maxValue - minValue);
      const y = padding.top + plotHeight - ratio * plotHeight;
      if (pointIndex === 0) {
        context.moveTo(x, y);
      } else {
        context.lineTo(x, y);
      }
    });
    context.stroke();
  });
}




